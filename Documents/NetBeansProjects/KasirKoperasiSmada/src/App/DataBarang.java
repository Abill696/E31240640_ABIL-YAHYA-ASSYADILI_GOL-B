    void loadData(String keyword) {
        DefaultTableModel model = new DefaultTableModel(
                new String[]{"ID Barang", "Nama Barang", "Harga", "Stok", "Barcode"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Membuat semua sel di tabel tidak bisa diubah
            }
        };

        try {
            String sql = "SELECT * FROM barang WHERE nama_barang LIKE ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, "%" + keyword + "%");
            ResultSet rs = pst.executeQuery();

            DecimalFormat kursIndonesia = (DecimalFormat) DecimalFormat.getCurrencyInstance(new Locale("in", "ID"));
            DecimalFormatSymbols formatRp = new DecimalFormatSymbols();
            formatRp.setCurrencySymbol("Rp. ");
            formatRp.setMonetaryDecimalSeparator(',');
            formatRp.setGroupingSeparator('.');
            kursIndonesia.setDecimalFormatSymbols(formatRp);
            kursIndonesia.setMaximumFractionDigits(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("ID_Barang"),
                    rs.getString("Nama_Barang"),
                    kursIndonesia.format(rs.getDouble("Harga")),
                    rs.getInt("Stok"),
                    rs.getString("Barcode")
                });
            }
            TblBarang.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }
    }

